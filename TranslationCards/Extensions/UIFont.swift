import UIKit

extension UIFont {
    static func robotoThin(size: CGFloat = 17.0) -> UIFont {
        UIFont(name: "Roboto-Thin", size: size)!
    }
    
    static func robotoRegular(size: CGFloat = 17.0) -> UIFont {
        UIFont(name: "Roboto-Regular", size: size)!
    }
}
